import { reactive } from 'vue';

/**
 * Tiny dependency-free i18n: a reactive locale + dictionaries.
 * Components call t('key', { params }) — the reactive read of
 * `state.locale` makes every consumer re-render on switch, no reload.
 * Team and player names are never translated.
 */

export const i18n = reactive({
    locale: localStorage.getItem('locale') ?? 'en',
});

export function setLocale(locale) {
    i18n.locale = locale;
    localStorage.setItem('locale', locale);
    document.documentElement.lang = locale;
}

export function t(key, params = {}) {
    const dict = messages[i18n.locale] ?? messages.en;
    let text = dict[key] ?? messages.en[key] ?? key;

    for (const [name, value] of Object.entries(params)) {
        text = text.replaceAll(`{${name}}`, value ?? '');
    }

    return text;
}

export function dateLocale() {
    return i18n.locale === 'tr' ? 'tr-TR' : 'en-GB';
}

